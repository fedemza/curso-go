package main

import "fmt"

func main() {

	num1 := 5
	num2 := 4

	// suma

	resultado := num1 + num2

	fmt.Println(resultado)

	// resta

	resultado = num1 - num2

	fmt.Println(resultado)

	// multiplicacion

	resultado = num1 * num2

	fmt.Println(resultado)

	// division

	resultado = num1 / num2

	fmt.Println(resultado)

	resultadoDivision := 5.0 / 4.0

	fmt.Println(resultadoDivision)

	// modulo

	resultado = num1 % num2

	fmt.Println(resultado)

}
